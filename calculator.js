// Creating basic math operation functions
let add = (n1, n2) => {
    return n1 + n2;
}

let subtract = (n1, n2) => {
    return n1 - n2;
}

let multiply = (n1, n2) => {
    return n1 * n2;
}

let divide = (n1, n2) => {
    return n1 / n2;
}

